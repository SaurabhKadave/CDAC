package in.cdac.ques8;


public class ArrayPrac {
		private int[] arr;
		
		public ArrayPrac(){
			arr = null;
		}
		public ArrayPrac(int size){
			arr = new int[size];
		}
		
		
		
		public void setArr(int[] arr) {
			this.arr = arr;
		}
		
		public int[] getArr() {
			return this.arr;
		}
		
			


}
